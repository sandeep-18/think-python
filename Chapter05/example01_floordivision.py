# Testing the difference between floor division (integer division) and regular division in 
# Python3

minutes = 105

# Floor Division - cuts the float part
floor_division_result = minutes//60
print(floor_division_result)

# Regular Division - keeps the float part
regular_division_result = minutes/60
print(regular_division_result)